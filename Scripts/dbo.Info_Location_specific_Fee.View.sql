CREATE VIEW [dbo].[Info_Location_specific_Fee]
AS
SELECT     dbo.Location.Location,

sum(case when  dbo.Location_Specific_Fees.Fee_type=35 then
	dbo.Location_Specific_Fees.Percentage_fee
	else
	0
end) CRF,

sum(case when dbo.Location_Specific_Fees.Fee_type=96 then
	dbo.Location_Specific_Fees.Per_Day_fee
	else
	0
end) VLF,

sum(case when  dbo.Location_Specific_Fees.Fee_type=39 then
	dbo.Location_Specific_Fees.Per_Day_fee
	else
	0
end) CFC

FROM         dbo.Location_Specific_Fees INNER JOIN
                      dbo.Location ON dbo.Location_Specific_Fees.Location_ID = dbo.Location.Location_ID
WHERE     (dbo.Location_Specific_Fees.Valid_To > GETDATE()) --and dbo.Location.Location_ID=16
group by dbo.Location.Location

